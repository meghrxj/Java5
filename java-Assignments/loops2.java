public class loops2 {

    public static void main(String[] args) {
        //for LOOPS 
        //1-100
        for(int i = 0; i <= 100; i++) {
            System.out.print(i);
        }

        //100-1 
        for(int j= 100; j >= 1; j-- ) {
            System.out.println(j);
        }


    }
}